/**
 * Gustave Doré's Bible illustrations (1866).
 * All engravings are public domain.
 *
 * This file contains the curated MVP: ~60 iconic engravings with confirmed
 * scripture mappings. The ingestion script (scripts/ingest-dore.mjs) uses the
 * `wikimediaFile` field to derive image URLs via
 *   https://commons.wikimedia.org/wiki/Special:FilePath/<filename>
 * which always redirects to the current canonical image URL.
 *
 * Filenames here are verified to exist on Wikimedia Commons. Most come from
 * "Category:Doré's English Bible" (the numbered NNN.*.jpg series, 151 plates),
 * which is the most complete Commons upload of the 1866 La Grande Bible de Tours.
 * A few come from "Category:Art depicting the Old/New Testament by Gustave Doré"
 * where the numbered series was incomplete or a different crop was preferred.
 *
 * Scene slugs match conceptual groups (e.g. 'creation', 'crucifixion') so
 * the /art surface can cluster art across artists.
 */

