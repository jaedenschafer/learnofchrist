// Rubens was the dominant Catholic painter of the Counter-Reformation in
// northern Europe: Antwerp Cathedral's triptychs (Raising of the Cross,
// Descent from the Cross, Assumption), the Chiesa Nuova altarpiece, the
// Whitehall cycles, and dozens of small panels for private devotion.
// This file collects his canonical biblical canvases — the two Antwerp
// Cathedral triptychs, the major altarpieces (Massacre of the Innocents,
// Samson and Delilah, Daniel, Meeting of Abraham and Melchizedek,
// Resurrection, Assumption), and a spread of Old and New Testament
// subjects spanning his full career.
