// Michelangelo Buonarroti — Sistine Chapel (1508–1512, 1536–1541) and
// Pauline Chapel (1542–1550) biblical cycles, plus the Doni Tondo.
//
// The Sistine ceiling is organized as nine central Genesis panels running
// altar → entrance, flanked by alternating seated prophets and sibyls,
// with four Old Testament salvation scenes in the corner pendentives.
// The Last Judgment fills the entire west altar wall (painted 25 years
// later). The Pauline Chapel frescoes — the Conversion of Saul and the
// Crucifixion of Peter — are Michelangelo's last paintings.
//
// The five sibyls are gentile oracles included in medieval Christian
// typology as pagan foreshadowings of the Messiah; we anchor them to
// Isaiah 11 (the shoot from Jesse) as a Messianic referent.

