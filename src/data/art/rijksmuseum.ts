//
// Unlike the per-artist Wikimedia sources (dore, tissot, caravaggio, etc.),
// this is a single cross-artist source — `source = 'rijksmuseum'` — because
// the Rijks holdings cross many hands and the image URLs resolve through the
// museum's IIIF server (iiif.micr.io) rather than Wikimedia Commons.
//
// Image URLs and object numbers were harvested programmatically and verified
// to return HTTP 200 at time of ingest. Scripture anchors are assigned by
// title-keyword regex against the curated English and Dutch titles.

